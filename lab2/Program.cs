namespace lab2
{
    // Определяем класс Program, который содержит точку входа в приложение
    class Program
    {
        // Определяем точку входа в приложение - статический метод Main
        static void Main(string[] args)
        {
            // Создаем экземпляр класса Regatta, который представляет регату. Инициализируем его списком яхт,
            // передав в конструктор объекты класса Yacht с заданными параметрами
            var regatta = new Regatta
            {
                new Yacht("Яхта 1", 20, 5, 1.5),
                new Yacht("Яхта 2", 25, 6, 1.6),
                new Yacht("Яхта 3", 18, 4, 1.4),
                new Yacht("Яхта 4", 30, 7, 1.7),
                new Yacht("Яхта 5", 22, 5, 1.5)
            };

            // Выводим в консоль заголовок "Призеры регаты:"
            Console.WriteLine("Призеры регаты:");

            // Выводим в консоль имена призеров регаты, получая их с помощью метода расширения Winners, который
            // возвращает список из трех яхт с наибольшей скоростью
            foreach (var yacht in regatta.Winners())
            {
                Console.WriteLine(yacht.Name);
            }

            // Выводим в консоль отношение скорости к осадке для самого медленного корабля, получая его с помощью
            // метода расширения SlowestYachtSpeedToDraftRatio. Форматируем вывод с двумя знаками после запятой с
            // помощью строки формата "F2"
            Console.WriteLine($"Отношение скорости к осадке для самого медленного корабля: {regatta.SlowestYachtSpeedToDraftRatio():F2}");
        }
    }
}

